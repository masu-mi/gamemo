package main

import (
	"bufio"
	"fmt"
	"io"
	"os"
	"sort"
	"strconv"
)

const (
	initialBufSize = 100000
	maxBufSize     = 1000000
)

var sc *bufio.Scanner

func initScanner(r io.Reader) *bufio.Scanner {
	buf := make([]byte, initialBufSize)

	sc := bufio.NewScanner(r)
	sc.Buffer(buf, maxBufSize)
	sc.Split(bufio.ScanWords) // bufio.ScanLines
	return sc
}

func main() {
	sc = initScanner(os.Stdin)
	fmt.Println(resolve(parseProblem()))
}

func parseProblem() int {
	n := scanInt(sc)
	ls := nextIntSlice(sc, n)
	num := 0
	for i := 0; i < n; i++ {
		for j := i + 1; j < n; j++ {
			for k := j + 1; k < n; k++ {
				li := ls[i]
				lj := ls[j]
				lk := ls[k]
				es := []int{li, lj, lk}
				sort.Sort(sort.IntSlice(es))
				if es[0] == es[1] || es[1] == es[2] {
					continue
				}
				if es[2] < es[0]+es[1] {
					num++
				}
			}
		}
	}
	return num
}

